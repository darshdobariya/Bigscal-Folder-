package com.example.gfgbasics.advanceview.diprecycleview.nested.demo;

public class DemoChild {

    private String text;
    private int color;

    public DemoChild(int color, String text) {
        this.color = color;
        this.text = text;
    }

    public int getColor() {
        return color;
    }

    public String getText() {
        return text;
    }
}
